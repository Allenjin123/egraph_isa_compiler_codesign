static int compare_strings(const char *lhs, const char *rhs) {
  while (*lhs != '\0' && *rhs != '\0' && *lhs == *rhs) {
      ++lhs; ++rhs;
  }
  unsigned char L = (unsigned char)*lhs;
  unsigned char R = (unsigned char)*rhs;
  return (L > R) - (L < R); 
}

static void copy_string(char *dest, const char *src, int dest_size) {
  if (dest_size == 0) return;
  int i = 0;
  while (i + 1 < dest_size && src[i] != '\0') {
      dest[i] = src[i];
      ++i;
  }
  dest[i] = '\0';
}

static void init_array(struct myStringStruct *array) {
  for (int i = 0; i < MAXARRAY; ++i) {
    copy_string(array[i].qstring, input_data[i], sizeof(array[i].qstring));
  }
}

static void swap_elements(struct myStringStruct *lhs, struct myStringStruct *rhs) {
  char tmp[sizeof(lhs->qstring)];
  for (size_t i = 0; i < sizeof(lhs->qstring); ++i) {
    tmp[i] = lhs->qstring[i];
  }
  for (size_t i = 0; i < sizeof(lhs->qstring); ++i) {
    lhs->qstring[i] = rhs->qstring[i];
  }
  for (size_t i = 0; i < sizeof(lhs->qstring); ++i) {
    rhs->qstring[i] = tmp[i];
  }
}

static void quicksort_range(struct myStringStruct *array, int left, int right) {
  while (left < right) {
    int i = left;
    int j = right;
    char pivot[128];
    copy_string(pivot, array[(left + right) / 2].qstring, sizeof(pivot));

    while (i <= j) {
      while (compare_strings(array[i].qstring, pivot) > 0) {
        ++i;
      }
      while (compare_strings(array[j].qstring, pivot) < 0) {
        --j;
      }
      if (i <= j) {
        swap_elements(&array[i], &array[j]);
        ++i;
        --j;
      }
    }

    if (left < j) {
      quicksort_range(array, left, j);
    }
    left = i;
  }
}

static void sort_array(struct myStringStruct *array) {
  if (MAXARRAY > 1) {
    quicksort_range(array, 0, MAXARRAY - 1);
  }
}

int main(void) {
  struct myStringStruct array[MAXARRAY];
  init_array(array);
  sort_array(array);
  unsigned long long checksum = 0ULL;
  for (int i = 0; i < MAXARRAY; ++i) {
    const char *s = array[i].qstring;
    while (*s != '\0') {
      checksum += (unsigned char)(*s);
      ++s;
    }
  }
  qsort_checksum = checksum;
  printf("\nSorting %d elements.\n\n", MAXARRAY);
  for (int i = 0; i < MAXARRAY; ++i) {
    printf("%s\n", array[i].qstring);
  }
  return (qsort_checksum == 10000) ? 1 : 0;
}
